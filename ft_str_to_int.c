unsigned int	ft_str_to_int(char *str)
{
	int				i;
	unsigned int	nn;

	i = 0;
	nn = 0;
	while (str[i])
	{
		nn = nn * 10 + (str[i] - '0');
		i++;
	}
	return (nn);
}
