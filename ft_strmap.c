#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	size_t	i;
	char	*d;

	i = 0;
	while (s[i])
		i++;
	d = malloc(sizeof(char) * i + 1);
	if (!d)
		return (0);
	i = 0;
	while (s[i])
	{
		d[i] = f(s[i]);
		i++;
	}
	d[i] = '\0';
	return (d);
}
